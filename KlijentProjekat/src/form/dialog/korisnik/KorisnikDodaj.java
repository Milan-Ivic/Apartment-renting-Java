    private void btnSacuajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuajActionPerformed
        try {
            if(txtIme.getText().isEmpty() || txtJMBG.getText().isEmpty() || txtPrezime.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena");
                return;
            }
            
             if(!samoSlova(txtIme.getText()) || !samoSlova(txtPrezime.getText()) || !samoCifre(txtJMBG.getText())){
                JOptionPane.showMessageDialog(this, "Ime i prezime moraju sadrzati samo slova, a JMBG samo cifre");
                return;
            }
            
            
            int korisnikID = Integer.parseInt(txtKorisnikID.getText());
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String JMBG = txtJMBG.getText();
            boolean status = false;
            ArrayList<Korisnik> korisnici = controller.Communication.getInstance().getAllKorisnik();
            for (Korisnik korisnik : korisnici) {
                if(korisnik.getJMBG().equals(JMBG)){
                
                    JOptionPane.showMessageDialog(this, "JMBG mora biti jedinstven.");
                    return;
                }
            }
           
            Korisnik k = new Korisnik(korisnikID, ime, prezime, JMBG, status);
            Object vracenKorisnik = controller.Communication.getInstance().updateKorisnik(k);
            
            if(!(boolean)vracenKorisnik){
            
                JOptionPane.showMessageDialog(this, "Sistem ne mo≈æe da zapamti korisnika.");
            }else{
            
                JOptionPane.showMessageDialog(this, "Sistem je uspesno zapamtio korisnika.");
            }
        } catch (IOException ex) {
            Logger.getLogger(KorisnikDodaj.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnSacuajActionPerformed

    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        try {
            // TODO add your handling code here:
           
            Korisnik ko = new Korisnik();
            ko.setIme("");
            ko.setPrezime("");
            ko.setJMBG("");
            ko.setStatus(false);
            
            Korisnik vracen = controller.Communication.getInstance().createKorisnik(ko);
            if(vracen==null){
            
                JOptionPane.showMessageDialog(this, "Sistem ne moze da kreira korisnika.");
            }else{
            
                JOptionPane.showMessageDialog(this, "Sistem je uspesno kreirao korisnika.");
                btnSacuaj.setEnabled(true);
                txtKorisnikID.setText(String.valueOf(vracen.getKorisnikID()));
                
            }
        } catch (IOException ex) {
            Logger.getLogger(KorisnikDodaj.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnKreirajActionPerformed

